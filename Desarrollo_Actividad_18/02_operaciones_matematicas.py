# Operaciones matemáticas

# a. Captura por teclado dos números enteros y reali operaciones de suma, resta, multiplicación y división
# b. Imprimir los resultados.

numero_uno = int(input("Ingresa el primer número: "))
numero_dos =int(input("Ingresa el segundo número: "))

suma = numero_uno + numero_dos
resta = numero_uno - numero_dos
multiplicacion = numero_uno * numero_dos
division = numero_uno / numero_dos

print(int(suma))
print(int(resta))
print(int(multiplicacion))
print(int(division))

